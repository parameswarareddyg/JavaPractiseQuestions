package com.java.operators;

public class AssignmentOperations {

	public static void main(String[] args) {
		byte b = 20;
		 b+=5;
		 System.out.println(b);
		 b-=5;
		 System.out.println(b);
		 b*=5;
		 System.out.println(b);
		 b/=5;
		 System.out.println(b);
		 b%=5;
		 System.out.println(b);
		 

	}

}
